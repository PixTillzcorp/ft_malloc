#include "./includes/malloc.h"

#define VALUE 1024

int main()
{
	int i;
    char *addr;

	i = 0;
	while (i < VALUE) {
		addr = (char *)mmap(NULL, VALUE, PROT_READ | PROT_WRITE, MAP_ANON | MAP_PRIVATE, -1, 0);
		addr[0] = 42;
		bzero(addr, VALUE);
        if (munmap((char *)addr, VALUE) < 0)
            ft_putendl("ERROR UNMAP");
		i++;
	}
//	show_alloc_mem();
	return (0);
}
