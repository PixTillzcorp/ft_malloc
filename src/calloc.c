#include "../includes/malloc.h"

void        *calloc(size_t count, size_t size)
{
    void    *ptr;
    size_t  len;

    if (!count || !size)
        return (NULL);
    len = count * size;
    if (!(ptr = malloc(len))) {
        ft_putendl("Malloc call in calloc failed.");
        return (NULL);
    }
    pthread_mutex_lock(&g_mutex);
    ft_bzero(ptr, len);
    pthread_mutex_unlock(&g_mutex);
    return (ptr);
}